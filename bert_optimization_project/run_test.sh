#!/bin/bash
################################################################################
# BERT优化项目 - 完整测试Pipeline
################################################################################

set -e  # 遇到错误立即退出

echo "================================================================================"
echo "BERT算子优化项目 - 自动化测试"
echo "================================================================================"
echo ""

# 检查CUDA
if ! command -v nvcc &> /dev/null; then
    echo "❌ 错误: 未找到CUDA (nvcc)"
    exit 1
fi

# 检查环境
if [ -z "$CONDA_DEFAULT_ENV" ]; then
    echo "⚠ 警告: 未激活conda环境"
    echo "请运行: source /usr/local/miniconda3/bin/activate pytorch_test"
    exit 1
fi

echo "✓ 环境检查通过"
echo "  - CUDA: $(nvcc --version | grep release | awk '{print $5}' | tr -d ',')"
echo "  - Conda环境: $CONDA_DEFAULT_ENV"
echo ""

# ============================================================================
# 步骤1: 编译优化的CUDA算子
# ============================================================================
echo "================================================================================"
echo "步骤1: 编译优化的CUDA算子"
echo "================================================================================"

cd custom_ops

if [ -f "../build/lib.linux-x86_64-cpython-*/bert_fused_ops*.so" ] || python -c "import bert_fused_ops" 2>/dev/null; then
    echo "✓ 已存在编译好的算子，跳过编译"
else
    echo "正在编译优化算子..."
    python setup.py install 2>&1 | tail -30
    
    if [ $? -eq 0 ]; then
        echo "✓ 编译成功"
    else
        echo "❌ 编译失败"
        exit 1
    fi
fi

cd ..
echo ""

# ============================================================================
# 步骤2: 运行性能测试
# ============================================================================
echo "================================================================================"
echo "步骤2: 运行性能测试"
echo "================================================================================"

echo ""
echo "▶ 运行性能对比测试 (无hooks, 5轮测试)"
echo "  预计耗时: 2-3分钟"
echo ""

python test_performance.py

if [ $? -eq 0 ]; then
    echo ""
    echo "✓ 测试完成！"
else
    echo ""
    echo "❌ 测试失败"
    exit 1
fi

# ============================================================================
# 总结
# ============================================================================
echo ""
echo "================================================================================"
echo "测试完成总结"
echo "================================================================================"
echo ""
echo "生成的文件:"
echo "  ✓ test_performance.py 的输出 (在终端)"
echo ""
echo "文档资料:"
echo "  📄 COMPLETE_OPTIMIZATION_SUMMARY.md  - 完整技术文档"
echo "  📄 QUICK_SUMMARY.txt                 - 快速总结"
echo "  📊 optimization_comparison.png        - 性能对比图"
echo ""
echo "核心代码:"
echo "  🔧 custom_ops/fused_ops.cu            - 优化的CUDA kernel"
echo "  🐍 models/bert_optimized.py           - 优化的BERT模型"
echo "  🧪 test_performance.py                - 性能测试代码"
echo ""
echo "================================================================================"

